/**
 * 定义基本常量的接口
 * 用于存储在整个应用程序中通用的常量值
 */
package com.spark.constant;

/**
 * BasicConstant接口定义了基本的常量
 * 主要用于存储日期格式化字符串等全局使用的常量值
 */
public interface BasicConstant {

    /**
     * 默认的日期和时间格式化字符串
     * 此常量用于在应用程序中统一日期和时间的显示格式
     */
    String DATE_DEFAULT_FORMATTER = "yyyy-MM-dd HH:mm:ss.SSS";

}
